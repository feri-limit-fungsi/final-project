function sendToWhatsapp() {
      let number = "+6285713133074"

      let name = document.getElementById("user-name").value;
      let alamat = document.getElementById("alamat-name").value;
      let produk = document.getElementById("produk-name").value;
      let harga = document.getElementById("harga-name").value;
      let keterangan = document.getElementById("keterangan-name").value;

      var url = "https://wa.me/" + number + "?text=" +
            "Nama Saya : " + name + "%0a" +
            "Alamat Saya : " + alamat + "%0a" +
            "Saya mau beli produk :" + produk + "%0a" +
            "Dengan Harga : " + harga + "%0a" +
            "Keterangan : " + keterangan + "%0a%0a "

      window.open(url, '_blank').focus();
}

